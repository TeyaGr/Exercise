
public class Casting {
//// type casting data types
	
	public static void main(String[] args) {
		
		long l = 10000;
		double d = 7.5;
		int i = 10;
		long a = i;
		double b = l;
		long o = 5215223;
		int c = (int) o;
		System.out.println(c);
		int d1 = (int) d;
		System.out.println(d1);
		byte by = (byte)o;
		float flo = 3.5555f;
		byte bu = (byte)flo;
		System.out.println(by + " " + bu);
				
	}
}
